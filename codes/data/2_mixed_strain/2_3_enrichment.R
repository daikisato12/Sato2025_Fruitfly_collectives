#### load packages ####
targetPackages <- c('tidyverse','BiocManager','wordcloud','ggnewscale','ggarchery','tidytext')
newPackages <- targetPackages[!(targetPackages %in% installed.packages()[,"Package"])]
if(length(newPackages)) install.packages(newPackages, repos = "http://cran.us.r-project.org")
targetPackages_Bioc <- c('ggupset','clusterProfiler','pathview','enrichplot','org.Dm.eg.db')
newPackages_Bioc <- targetPackages_Bioc[!(targetPackages_Bioc %in% installed.packages()[,"Package"])]
if(length(newPackages_Bioc)) BiocManager::install(newPackages_Bioc)
for(package in c(targetPackages, targetPackages_Bioc)) library(package, character.only = T)
organism = "org.Dm.eg.db"

#### load dataset ####
genelist_background <- read.delim("../data/0_genome/dgrp2_2pop_SNPlist_geneid.txt", h=F)
genelist_background <- as.vector(genelist_background[,1])

#### 1kbp_performanceDE_sec3_regress_pi_grm_plus ####
for_sec <- 3
genelist_1kbp_performanceDE_regress_pi_grm_plus = read.delim(paste0("../data/2_mixed_strain/gwas/result/tophits/tophits_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus_geneid_2000.txt"), header = FALSE)
genelist_1kbp_performanceDE_regress_pi_grm_plus <- as.vector(genelist_1kbp_performanceDE_regress_pi_grm_plus[,1])
dir.create(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus"), recursive = T)

##### BP #####

go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus <- enrichGO(gene = genelist_1kbp_performanceDE_regress_pi_grm_plus,
                                                                         universe = genelist_background,
                                                                         OrgDb = organism, 
                                                                         keyType = 'FLYBASE',
                                                                         readable = T,
                                                                         ont = "BP",
                                                                         pvalueCutoff = 0.05, 
                                                                         qvalueCutoff = 0.10)
head(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus)
df_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus <- go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus@result %>%
  filter(p.adjust < 0.05) %>%
  mutate(geneID = str_replace_all(geneID, "/", ", "),
         Type = "BP")
write.table(df_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus, paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/df_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus.tsv"), quote = FALSE, sep = "\t", row.names = FALSE)


g1_BP_1kbp_performanceDE_regress_pi_grm_plus <- upsetplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus)
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/UpsetPlot_BP.pdf"), g1_BP_1kbp_performanceDE_regress_pi_grm_plus, h=6, w=10)

g2_BP_1kbp_performanceDE_regress_pi_grm_plus <- barplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus, 
                                                                 drop = TRUE, 
                                                                 showCategory = 10, 
                                                                 title = "GO Biological Process",
                                                                 font.size = 8)
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/BarPlot_BP.pdf"), g2_BP_1kbp_performanceDE_regress_pi_grm_plus, h=3, w=5)

g3_BP_1kbp_performanceDE_regress_pi_grm_plus <- dotplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus, orderBy = "x")
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/DotPlot_BP.pdf"), g3_BP_1kbp_performanceDE_regress_pi_grm_plus, h=4, w=6)

g4_BP_1kbp_performanceDE_regress_pi_grm_plus <- emapplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus %>% pairwise_termsim())
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/EncrichmentMap_BP.pdf"), g4_BP_1kbp_performanceDE_regress_pi_grm_plus, h=6, w=8)

g5_BP_1kbp_performanceDE_regress_pi_grm_plus <- goplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus, showCategory = 10)
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/EncrichedGOGraph_BP.pdf"), g5_BP_1kbp_performanceDE_regress_pi_grm_plus, h=6, w=8)

g6_BP_1kbp_performanceDE_regress_pi_grm_plus <- cnetplot(go_enrich_BP_1kbp_performanceDE_regress_pi_grm_plus, categorySize="pvalue")
ggplot2::ggsave(paste0("../data/2_mixed_strain/gwas/result/enrichment/clusterProfiler_1kbp_performanceDE_", for_sec, "sec_regress_pi_grm_plus/CategoryNet_BP.pdf"), g6_BP_1kbp_performanceDE_regress_pi_grm_plus, h=12, w=10)

